#  Most common functions in python, related to numbers:

my_num = -5
print(abs(my_num))  # abs stands for absolute value --> see chatGPT for info.

print(pow(3, 2))  # This is "to the power of". So this is 3 to the power of 2

print(max(3, 4))  # Returning the bigger value
print(min(2, 6))  # Returning the smaller value
print(round(3.6))  # Rounding a number

# To gain access to more math functions in python, the following has to be typed into the file:
from math import *

print (floor(3.7))  # Floors the number
print (ceil(3.2))  # Raises/rounds up
print(sqrt(36))  # Square root